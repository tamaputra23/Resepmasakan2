package com.example.user.resepmasakan;

public class lokasi {
    private String title;
    private String alamat;
    private String harga;
    private String jambuka;
    private String image;
    private String keyloc;
    Double koorlat;
    Double koorlong;

    public lokasi() {

    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getHarga() {
        return harga;
    }

    public void setHarga(String harga) {
        this.harga = harga;
    }

    public String getJambuka() {
        return jambuka;
    }

    public void setJambuka(String jambuka) {
        this.jambuka = jambuka;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
    public String getKeyloc() {
        return keyloc;
    }

    public void setKeyloc(String keyloc) {
        this.keyloc = keyloc;
    }
    public Double getKoorlat() {
        return koorlat;
    }

    public void setKoorlat(Double koorlat) {
        this.koorlat = koorlat;
    }
    public Double getKoorlong() {
        return koorlong;
    }

    public void setKoorlong(Double koorlong) {
        this.koorlong = koorlong;
    }


}
